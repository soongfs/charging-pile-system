package com.bupt.charging.enums;

/**
 * 调度模式开关（详细需求第 3 条基础调度 + 第 8 条可选加分扩展调度）。
 *
 * <p>默认 {@link #BASIC}：保持验收用例所依赖的基础调度行为不变。
 * 扩展模式仅用于可选加分演示，按详细需求 8c「不考虑改请求与充电桩故障」。</p>
 */
public enum SchedulingMode {
    /** 基础调度（必做）：FCFS 选车 + 单车「完成充电所需时间最短」选桩。 */
    BASIC,
    /** 8a 单次调度总时长最短：充电区多空位时按最短作业优先(SPT)选车，使这组车累计完成时长最短。 */
    SINGLE_SHORTEST,
    /** 8b 批量调度总时长最短：满站触发，不区分快慢充，贪心代价矩阵求整批总完成时长最短。 */
    BATCH_SHORTEST
}
